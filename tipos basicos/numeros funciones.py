import math  # Importa el modulo math

print(round(3.1415926535, 2))  # Redondea a 2 decimales dando 3.14

print(abs(-10))  # Valor absoluto de -10 dando 10

print(math.ceil(4.2))  # Redondea al entero mayor dando 5

print(math.floor(4.99999999))  # Redondea al entero menor dando 4

print(math.isnan(10))  # Devuelve True si el valor es NaN (Not a Number)

print(math.pow(2, 3))  # Devuelve 2 elevado a 3 dando 8

print(math.sqrt(16))  # Devuelve la raiz cuadrada de 16 dando 4

