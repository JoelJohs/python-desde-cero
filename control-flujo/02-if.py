edad = 25

# Condicional else-if (elif)
if edad > 50: # si la condición es verdadera
    print("puede ver la película con descuento") # se ejecuta este código
elif edad > 17: # elif es una abreviación de else if
    print("puedes ver la película")
else: # si la condición es falsa
    print("No puede ver la película") # se ejecuta este código
# Fin del condicional

print("fin del programa")
